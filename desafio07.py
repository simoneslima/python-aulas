# Solicitar as notas ao usuário
nota1 =float(input('Digite a primeira nota:'))
nota2 =float(input('digite a segunda nota:'))
# Calcular a média
media = (nota1 + nota2) / 2
# Exibir a média com duas casas decimais
print('A media da nota {:.1f}, e da nota {:.1f}, é igual a {:.1f}'.format(nota1, nota2, media))




